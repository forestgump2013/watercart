package tyt.beep.model;

public class Address {
	
	int id;
	String name;
	String mobile;
	String address;
	
	public Address(){
		
	}
	
	public void setId(int id){
		this.id=id;
	}
	
	public void setName(String name){
		this.name=name;
	}
	
	public void setMobile(String mobile){
		this.mobile=mobile;
	}
	
	public void setAddress(String adr){
		this.address=adr;
	}

}
